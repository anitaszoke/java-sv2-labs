package defaultconstructor.chess;

public class Player {
    private String name;
    private String mail;
    private String sign;

    public String getName() {
        return name;
    }

    public String getMail() {
        return mail;
    }

    public String getSign() {
        return sign;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }
}
